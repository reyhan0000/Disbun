        function togglePassword(inputId, iconId) {
            const input = document.getElementById(inputId);
            const icon  = document.getElementById(iconId);

            if (input.type === 'password') {
                input.type = 'text';
                icon.innerHTML = eyeClosedPath;
            } else {
                input.type = 'password';
                icon.innerHTML = eyeOpenPath;
            }
        }
